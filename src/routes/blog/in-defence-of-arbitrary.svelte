<p>
  There are few philosophers today who have a truly profound grasp of the
  universe. Individuals with such inherent wisdom, that to ignore their advice
  is an exercise in folly.
</p>

<p>So, when I heard the Aristotle of the 21st century say these words:</p>

<Quote cite="Taylor Swift">
  This is a new year. A new beginning. And things will change.
</Quote>

<p>I listened.</p>

<p>
  While consulting scholars on the many interpretations of Swift’s words, I was
  surprised to find a large number of people angry about the mere existence of
  New Year’s resolutions.
</p>

<p>
  You may be surprised to hear that anyone on the internet could be angry, but a
  quick Twitter search for “new year’s resolutions”, “tabs vs spaces”, or
  literally anything at all — reveals a barrage of angry tweeters.
</p>

<Quote cite="Annoying People on Twitter">
  New Year’s resolutions are pointless. A good developer hones his talents
  constantly, not waiting until some arbitrary calendar date.
</Quote>

<p>
  The conclusion that New Year’s resolutions are arbitrary isn’t wrong. But, at
  the same time, this sort of gripe largely misses the point of goals, and
  incorrectly assumes that arbitrary equates to worthless.
</p>

<p>So, in defence of the arbitrary…</p>

<p>
  Lots of things in life are arbitrary. Shaking someone’s hand is arbitrary, as
  are birthdays, and the unwritten rule in Mario Party to make a Waluigi NPC and
  then hate him, passionately and unconditionally.
</p>

<img
  src="./blog/in-defence-of-arbitrary/day-is-arbitrary.gif"
  alt="River saying “Day is a vestigial mode of time measurement based on solar
  cycles. It's not applicable. I didn't get you anything.”" />

<p>
  While shaking someone’s hand is arbitrary, it still serves a useful function.
  If someone refuses to shake my hand, I can deduct that they probably dislike
  me. If someone slaps my hand away, and goes for a hug, I can tell that I’m in
  for one hell of an uncomfortable job interview.
</p>

<p>
  We often depend on the arbitrary in tech, too. A sprint kick-off meeting is
  nothing more than an arbitrary time to reflect on your team’s current
  trajectory, and make new goals.
</p>

<p>
  When your company aims to hit 100,000 X-events by Q3, that’s not because
  someone sat down and found that 100,000 is the exact optimum number required
  for maximum X-ness. It’s just a nice goal. Arbitrary, but aiming for some
  ethereal “more” is less satisfying than “100,000”.
</p>

<p>
  Sprint and quarterly goals are just New Year’s resolutions, but for smaller
  periods of time, and involving less alcohol.
</p>

<p>
  There is a lot of evidence showing that humans are much more effective at
  achieving their goals when
  <a
    href="https://erationality.media.mit.edu/papers/dan/eRational/Dynamic%20preferences/deadlines.pdf">
    those goals have clear time-bounds,
  </a>
  and when they
  <a href="https://academic.oup.com/jpart/article-abstract/15/4/529/991007">
    are specific.
  </a>
</p>

<p>
  If you don’t want January 1st to be your arbitrary starting point, that’s
  fine. But embrace the arbitrary, don’t fear it! Set two random dates in your
  calendar as a start and finish, and go wild. Achieve your goals.
</p>

<p>
  For me, I love reviewing goals for January 1st. Nothing gets a New Year’s Eve
  party going like asking for the music to be turned down while you extrapolate
  some numbers to confirm feasibility.
</p>

